package important;

public class P8 {
	
	private String name;
	private int salary;
	private int id;
	P8(String name,int salary,int id){
		this.name=name;
		this.salary=salary;
		this.id=id;
	}
	public String getName() {
		return this.name;
	}
	public int getSalary() {
		return this.salary;
	}
	public int getId() {
		return this.id;
	}
}
